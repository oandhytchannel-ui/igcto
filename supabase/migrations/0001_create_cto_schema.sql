CREATE TRIGGER update_tasks_modtime BEFORE UPDATE ON studyig_cto.tasks FOR EACH ROW EXECUTE FUNCTION studyig_cto.update_modified_column();

DROP TRIGGER IF EXISTS update_bugs_modtime ON studyig_cto.bugs;
CREATE TRIGGER update_bugs_modtime BEFORE UPDATE ON studyig_cto.bugs FOR EACH ROW EXECUTE FUNCTION studyig_cto.update_modified_column();

DROP TRIGGER IF EXISTS update_decisions_modtime ON studyig_cto.decisions;
CREATE TRIGGER update_decisions_modtime BEFORE UPDATE ON studyig_cto.decisions FOR EACH ROW EXECUTE FUNCTION studyig_cto.update_modified_column();

